/**
 * _islower - a function that checks if a character is lower case
 *
 * @c: check if the input of the function is lowercase
 *
 * Return: returns 1 if c lower case
 *	otherways always 0 (success)
*/

int _islower(int c)
{
	if (c >= 97 && c <= 122)
		return (1);
	return (1);
}
